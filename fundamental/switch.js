function testingSwitch() {
  var nilai = 10;
  // switch itu kayak tombol, kalo case nya sesuai sama parameter nilai,
  // kepencet switch nya
  switch (nilai) {
    case 10:
      console.log("Sangat bagus");
      break;
    case 8:
      console.log("Baik");
      break;
    case 6:
      console.log("cukup");
      break;
    default:
      console.log("Lainnya");
  }
}

testingSwitch();
